#!/usr/bin/env python
#Estudo de laço de repetição
a = 6
b = 5
c = 1
x = a

#!/usr/bin/env python
#x = 0
while c < b :
    print("Entra o novo valor de x: {}".format(x))
    print(("x recebe {} e multiplica por 'a=6' :".format(x)))
    x = x*a
    c = c + 1
    print ("Resultado do while: {}".format(x)+"\n")
    continue
        
print("Final.")
